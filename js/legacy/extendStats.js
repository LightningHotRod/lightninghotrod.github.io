const queryString = window.location.search;
const urlParams = new URLSearchParams(queryString);
const hasName = urlParams.has('name');
async function getSessionName() {
	const response = await fetch('https://api.blooket.com/api/users/verify-session', {
		method: "GET",
		headers: {
			"accept": "application/json, text/plain, */*",
			"accept-language": "en-US,en;q=0.9,ru;q=0.8",
		},
		credentials: "include"
	});
	const data = await response.json();

	return data.name;
};
async function getName() {
	if (hasName) {
		const userName = urlParams.get('name')
		return userName
	} else if (!(hasName)) {
		localStorage.washere = (await getSessionName());
		const userName = localStorage.washere;
		return userName
	};
};

async function getInfo(blooketName) {
	const response = await fetch('https://api.blooket.com/api/users?name=' + blooketName, {
		headers: {
			"referer": "https://www.blooket.com/",
			"content-type": "application/json"
		},
		credentials:"include"
	});
	const data = await response.json();
	return data;
};

